    private void showData(DataSnapshot dataSnapshot) {
        for(DataSnapshot data : dataSnapshot.getChildren()){
            UserInfo info = new UserInfo();
            info.setCompanyName(data.getValue(UserInfo.class).getCompanyName());

            adminCompanyList.add(data.getValue(UserInfo.class).getCompanyName());
        }
        final AdminCustomAdapter adapter = new AdminCustomAdapter(getContext(), adminCompanyList);
        ItemTouchHelper.SimpleCallback touchHelper = new ItemTouchHelper.SimpleCallback(0, ItemTouchHelper.RIGHT) {
            @Override
            public boolean onMove(@NonNull RecyclerView recyclerView, @NonNull RecyclerView.ViewHolder viewHolder, @NonNull RecyclerView.ViewHolder viewHolder1) {
                return false;
            }

            @Override
            public void onSwiped(@NonNull RecyclerView.ViewHolder viewHolder, int i) {
                adminCompanyList.remove(viewHolder.getAdapterPosition());
                adapter.notifyDataSetChanged();
            }
        };

        new ItemTouchHelper(touchHelper).attachToRecyclerView(recycler);
        recycler.setLayoutManager(new LinearLayoutManager(getContext()));
        recycler.setAdapter(adapter);
    }



